def is_prime(n):
    if n <= 1:
        return False
    if n <= 3:
        return True
    if n % 2 == 0 or n % 3 == 0:
        return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0:
            return False
        i += 6
    return True
test = int(input().strip())
for i in range (test):
    n = input().strip()
    print("YES" if is_prime(int(n[-4:])) else "NO")